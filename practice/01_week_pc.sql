use pc;

-- 1 --
SELECT MODEL, SPEED AS MHz, HD AS GB
FROM PC
WHERE PRICE < 1200;

-- 2 --
SELECT MODEL, PRICE * 1.1 AS EURO
FROM LAPTOP
ORDER BY EURO;

-- 3 --
SELECT MODEL, SPEED, HD
FROM PC
WHERE PRICE > 1000;

-- 4 --
SELECT *
FROM PRINTER
WHERE COLOR = 'y';

-- 5 --
SELECT MODEL, SPEED, HD
FROM PC
WHERE (CD = '12x' OR CD = '16x')
AND PRICE < 2000;

-- 6 --
SELECT CODE, MODEL, (SPEED + RAM + 10 * SCREEN) AS RATING
FROM LAPTOP
ORDER BY RATING, CODE;
